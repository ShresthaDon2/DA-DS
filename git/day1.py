# Git and Github 

# Git is a VCS that keeps the track of our project code 
# Github is an online platform to store our project code 


# Steps to follow 
# 1. Initialize the git => git init(only one time)
# 2. Connect system folder with github repository => git remote add origin {ssh_key} (oly one time for a project)
# 3. Add files to github => git add {file_name}. To add all files, we use => git add.
# 4. Commit the files  => git commit -m "Message"
# 5. Push the git to github => git push -u origin {branch_name}
# -u => Upstream
# At the first push, it is recommended to use => git push -u origin{branch name}

